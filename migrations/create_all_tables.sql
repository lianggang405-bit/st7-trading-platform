CREATE OR REPLACE FUNCTION update_users_updated_at()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = NOW();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS users_updated_at_trigger ON users;
CREATE TRIGGER users_updated_at_trigger
BEFORE UPDATE ON users
FOR EACH ROW
EXECUTE FUNCTION update_users_updated_at();

-- 添加表注释
COMMENT ON TABLE users IS '用户表，存储用户基本信息';
COMMENT ON COLUMN users.account_type IS '账户类型：demo-模拟账户，real-正式账户';
COMMENT ON COLUMN users.balance IS '账户余额';
COMMENT ON COLUMN users.credit_score IS '信用分';


-- =====================================================
-- 3. applications 表（申请表，用于实名认证等）
-- =====================================================
