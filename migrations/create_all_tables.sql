-- =====================================================
-- 4. crypto_addresses 表（加密货币地址表）
-- =====================================================
CREATE TABLE IF NOT EXISTS crypto_addresses (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  currency VARCHAR(20) NOT NULL,
  protocol VARCHAR(20) NOT NULL, -- TRC20, ERC20, BEP20 等
  address TEXT NOT NULL,
  network VARCHAR(50),
  min_amount NUMERIC(20, 8) DEFAULT 0,
  max_amount NUMERIC(20, 8) DEFAULT 999999999,
  fee NUMERIC(20, 8) DEFAULT 0,
  fee_type VARCHAR(10) DEFAULT 'fixed' CHECK (fee_type IN ('fixed', 'percentage')),
  status VARCHAR(20) NOT NULL DEFAULT 'active' CHECK (status IN ('active', 'inactive')),
  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
  updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 创建索引
CREATE INDEX IF NOT EXISTS idx_crypto_addresses_currency ON crypto_addresses(currency);
CREATE INDEX IF NOT EXISTS idx_crypto_addresses_status ON crypto_addresses(status);

-- 创建触发器自动更新 updated_at 字段
CREATE OR REPLACE FUNCTION update_crypto_addresses_updated_at()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = NOW();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS crypto_addresses_updated_at_trigger ON crypto_addresses;
CREATE TRIGGER crypto_addresses_updated_at_trigger
BEFORE UPDATE ON crypto_addresses
FOR EACH ROW
EXECUTE FUNCTION update_crypto_addresses_updated_at();

-- 添加表注释
COMMENT ON TABLE crypto_addresses IS '加密货币收款地址表';


-- =====================================================
-- 5. market_adjustments 表（市场调控记录表）
-- =====================================================
CREATE TABLE IF NOT EXISTS market_adjustments (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  action VARCHAR(50) NOT NULL,
  symbol VARCHAR(20) NOT NULL,
  before_price NUMERIC(20, 8) NOT NULL,
  after_price NUMERIC(20, 8) NOT NULL,
  change_percent VARCHAR(20),
  created_by VARCHAR(100) NOT NULL,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 创建索引
CREATE INDEX IF NOT EXISTS idx_market_adjustments_symbol ON market_adjustments(symbol);
CREATE INDEX IF NOT EXISTS idx_market_adjustments_created_at ON market_adjustments(created_at DESC);

-- 添加表注释
COMMENT ON TABLE market_adjustments IS '市场调控记录表';


-- =====================================================
-- 6. credit_adjustments 表（信用调整记录表）
-- =====================================================
CREATE TABLE IF NOT EXISTS credit_adjustments (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id INTEGER NOT NULL,
  before_score INTEGER NOT NULL,
  after_score INTEGER NOT NULL,
  change_value INTEGER NOT NULL,
  reason TEXT NOT NULL,
  created_by VARCHAR(100) NOT NULL,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 创建索引
CREATE INDEX IF NOT EXISTS idx_credit_adjustments_user_id ON credit_adjustments(user_id);
CREATE INDEX IF NOT EXISTS idx_credit_adjustments_created_at ON credit_adjustments(created_at DESC);

-- 添加表注释
COMMENT ON TABLE credit_adjustments IS '信用调整记录表';


